@torch.no_grad()
def inspect_prediction_stats(model, dataset, n=12):
    model.eval()
    rows = []

    indices = np.linspace(0, len(dataset) - 1, n, dtype=int)

    for idx in indices:
        image, mask = dataset[idx]

        x = image.unsqueeze(0).to(device)
        logits = model(x)[0]
        logits = F.interpolate(
            logits,
            size=mask.shape[-2:],
            mode="bilinear",
            align_corners=False,
        )

        prob = torch.softmax(logits, dim=1)[0, 1].cpu()
        gt = mask.cpu()

        valid = gt != 255
        prob_v = prob[valid]
        gt_v = gt[valid].float()

        pred_05 = (prob_v >= 0.5).float()
        pred_03 = (prob_v >= 0.3).float()
        pred_01 = (prob_v >= 0.1).float()

        inter = ((pred_05 == 1) & (gt_v == 1)).sum().item()
        union = ((pred_05 == 1) | (gt_v == 1)).sum().item()
        iou_05 = inter / max(union, 1)

        rows.append({
            "idx": idx,
            "gt_fg_ratio": gt_v.mean().item(),
            "prob_min": prob_v.min().item(),
            "prob_mean": prob_v.mean().item(),
            "prob_max": prob_v.max().item(),
            "pred_fg@0.5": pred_05.mean().item(),
            "pred_fg@0.3": pred_03.mean().item(),
            "pred_fg@0.1": pred_01.mean().item(),
            "IoU@0.5": iou_05,
        })

    return pd.DataFrame(rows)

