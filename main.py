def satnet_loss(preds, mask, weights):
    total_loss = torch.zeros((), device=mask.device)

    for pred, weight in zip(preds, weights):
        pred = F.interpolate(
            pred,
            size=mask.shape[-2:],
            mode="bilinear",
            align_corners=False,
        )
        total_loss = total_loss + weight * criterion(pred, mask)

    return total_loss

