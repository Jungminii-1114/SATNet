@torch.no_grad()
def evaluate_single_head(model, loader, head_idx=0, desc="Test"):
    model.eval()

    num_thresholds = int(config.get("metric_num_thresholds", 256))
    beta_square = float(config.get("f_beta_square", 0.3))
    ignore_index = int(config.get("ignore_index", 255))
    eps = 1e-7

    state = {
        "loss_sum": 0.0,
        "loss_count": 0,
        "iou_sum": 0.0,
        "mae_sum": 0.0,
        "precision_sum": torch.zeros(num_thresholds),
        "recall_sum": torch.zeros(num_thresholds),
        "count": 0,
    }

    thresholds = torch.linspace(0, 1, num_thresholds, device=device)

    for image, mask in tqdm(loader, desc=f"{desc} head={head_idx}", leave=False):
        image = image.to(device)
        mask = mask.to(device)

        preds = model(image)
        logits = preds[head_idx]

        logits = F.interpolate(
            logits,
            size=mask.shape[-2:],
            mode="bilinear",
            align_corners=False,
        )

        loss = criterion(logits, mask)

        bs = image.size(0)
        state["loss_sum"] += loss.item() * bs
        state["loss_count"] += bs

        prob = torch.softmax(logits, dim=1)[:, 1]

        for b in range(prob.size(0)):
            valid = mask[b] != ignore_index
            if valid.sum() == 0:
                continue

            p = prob[b][valid]
            g = (mask[b][valid] > 0).float()
            g_bool = g.bool()

            hard_05 = p > 0.5
            inter = (hard_05 & g_bool).sum().float()
            union = hard_05.sum().float() + g_bool.sum().float() - inter
            iou = inter / (union + eps) if union > 0 else torch.tensor(0.0, device=device)

            mae = torch.abs(p - g).mean()

            hard = p.unsqueeze(0) > thresholds.unsqueeze(1)
            tp = (hard & g_bool.unsqueeze(0)).sum(dim=1).float()
            pred_sum = hard.sum(dim=1).float()
            gt_sum = g_bool.sum().float()

            precision = (tp + eps) / (pred_sum + eps)
            recall = (tp + eps) / (gt_sum + eps)

            state["iou_sum"] += iou.item()
            state["mae_sum"] += mae.item()
            state["precision_sum"] += precision.detach().cpu()
            state["recall_sum"] += recall.detach().cpu()
            state["count"] += 1

    count = max(state["count"], 1)
    precision = state["precision_sum"] / count
    recall = state["recall_sum"] / count

    f_beta = (1 + beta_square) * precision * recall / (
        beta_square * precision + recall + eps
    )

    return {
        "head": f"p{head_idx}",
        "loss": state["loss_sum"] / max(state["loss_count"], 1),
        "iou": state["iou_sum"] / count,
        "f": f_beta.max().item(),
        "mae": state["mae_sum"] / count,
        "best_threshold": (torch.argmax(f_beta).item() / max(num_thresholds - 1, 1)),
    }


